public class Encapsulamento {
    // As variáveis estão public - Acessível de qualquer lugar
    public String name1; 
    public int age1;
    // As variáveis estão private - Acessível apenas dentro da classe.
    private String name2; 
    private int age2;
    // As variáveis estão protected - Acessível dentro do mesmo pacote e por subclasses.
    protected String name3; 
    protected int age3;
}